"""
str - string
* pode-se usar aspas simples ou duplas
"""
print('Alguma coisa')
print("Alguma coisa")

""" Exemplo de como trabalhar como aspas duplas e simples dentro do comando 
    primeiro exemplo podemos iniciar com a aspa simples escrever o texto com a aspas dupala e finalizar com a aspa simples
    segundo exemplo podemos começar com as aspas dupla escreve o texto com a aspa simples e finalizar com as aspas duplas
"""
print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')

## podemos utilizar caracter de escape
print('Exemplo de com aspas simples e dupla ao mesmo tempo. \'string\' e "string"')

## podemos utilizar uma letra r dentro do comando print para faze-lo ignorar os comandos
print(r'Geralmente o \n é utilizado para fazer uma quebra de linha mas como coloquei o r neste comando print ele esta aparecendo no meio do texto.')


